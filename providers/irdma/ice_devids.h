#define ICE_DEV_ID_C822N_BACKPLANE      0x1890
/* Intel(R) Ethernet Connection C822N for QSFP */
#define ICE_DEV_ID_C822N_QSFP           0x1891
/* Intel(R) Ethernet Connection C822N for SFP */
#define ICE_DEV_ID_C822N_SFP            0x1892

#endif /* ICE_DEVIDS_H */

